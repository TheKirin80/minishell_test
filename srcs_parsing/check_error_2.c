#include "../headers/parsing.h"

int	check_space(char *s)
{
	int	i;

	i = 0;
	while (s && s[i] != '\0')
	{
		if (s[i] != ' ' && s[i] != '\t' && s[i] != '\n'
			&& s[i] != '\r' && s[i] != '\v' && s[i] != '\f')
			return (OK);
		i++;
	}
	return (ERROR);
}
